public enum PaperState {
    CREATED,
    SUBMITTED,
    REVIEWED,
    REJECTED,
    APPROVED,
    ACCEPTED
}

/*
CREATED: Initial state when a paper is created.
SUBMITTED: The paper has been formally submitted to the conference.
REVIEWED: The paper has been reviewed by the assigned reviewers.
REJECTED: The paper has been rejected after the review process.
APPROVED: The paper has been approved after the review process.
ACCEPTED: The paper has been finally accepted and can be presented in the conference.
 */